def largest(num1, num2):
    if num1 >= num2:
        return num1
    else:
        return num2

def smallest(num1, num2):
    if num1 <= num2:
        return num1
    else:
        return num2

def equal(num1,num2):
    if num1 == num2:
        print("Is equal")
        return True
    else:
        print("Not Equal")
        return False